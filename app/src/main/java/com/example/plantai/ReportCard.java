package com.example.plantai;

public class ReportCard {

    private String mText,mDate;

    public ReportCard(String mText, String mDate) {
        this.mText = mText;
        this.mDate = mDate;
    }

    public String getmText() {
        return mText;
    }

    public String getmDate() {
        return mDate;
    }
}
